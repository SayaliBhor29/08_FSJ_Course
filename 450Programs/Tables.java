import java.util.*;
class Tables{
  public static void main(String args[])
{
     int i = 0, j=0, n = 10;
     for(i=1; i<=n; i++)
     {
        for(j=1; j<=n; j++)
       {
          System.out.printf(" %d", i*j);
        }
       System.out.println("\n");
    }
   }
}
             
